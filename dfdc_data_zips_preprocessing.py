import numpy as np

def read_frames_even(video_path, max_frames=MAX_FRAMES):
    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        return []
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT) or 0)
    if total <= 0:
        frames = []
        while len(frames) < max_frames:
            ok, f = cap.read()
            if not ok: break
            frames.append(cv2.cvtColor(f, cv2.COLOR_BGR2RGB))
        cap.release()
        return frames
    indices = np.linspace(0, total - 1, num=min(max_frames, total), dtype=int)
    frames = []
    idx_set = set(indices.tolist())
    i = 0
    next_idx = 0
    current_target = indices[next_idx] if len(indices) > 0 else None
    while cap.isOpened() and len(frames) < len(indices):
        ok, f = cap.read()
        if not ok: break
        if i == current_target:
            frames.append(cv2.cvtColor(f, cv2.COLOR_BGR2RGB))
            next_idx += 1
            if next_idx >= len(indices): break
            current_target = indices[next_idx]
        i += 1
    cap.release()
    return frames

@torch.no_grad()
def extract_spatial_faces(frames, min_face=MIN_FACE):
    face_crops = []
    face_embeddings = []
    for frame in frames:
        faces = face_app.get(frame)
        if not faces:
            face_crops.append(None)
            continue
        f = faces[0]
        if f.bbox is None:
            face_crops.append(None)
            continue
        x1, y1, x2, y2 = map(int, f.bbox)
        if x2 <= x1 or y2 <= y1:
            face_crops.append(None); continue
        if (x2-x1) < min_face or (y2-y1) < min_face:
            face_crops.append(None); continue
        h, w, _ = frame.shape
        x1, y1 = max(0,x1), max(0,y1)
        x2, y2 = min(w,x2), min(h,y2)
        crop = frame[y1:y2, x1:x2]
        if crop.size == 0:
            face_crops.append(None); continue
        face_crops.append(crop)
        # preprocess for spatial_model
        img = cv2.resize(crop, (224,224))
        img = torch.from_numpy(img).permute(2,0,1).float()/255.0
        if FP16:
            img = img.half()
        img = img.unsqueeze(0).to(DEVICE)
        feat = spatial_model(img)
        face_embeddings.append(feat.squeeze(0).cpu())
    if face_embeddings:
        spatial = torch.stack(face_embeddings)
    else:
        spatial = None
    return face_crops, spatial


def extract_motion(frames):
    feats = []
    prev = None
    for f in frames:
        g = cv2.cvtColor(f, cv2.COLOR_RGB2GRAY)
        if prev is None:
            feats.append(torch.zeros(256))
        else:
            diff = cv2.absdiff(prev, g)
            feats.append(torch.tensor(diff.mean()).repeat(256))
        prev = g
    return torch.stack(feats)

@torch.no_grad()
def extract_audio(video_path, frames_len, sr=AUDIO_SR):
    # use ffmpeg via subprocess to extract wav robustly
    tmp_wav = os.path.join(TMP_DIR, f"audio_{int(time.time()*1000)}.wav")
    cmd = ['ffmpeg', '-y', '-i', video_path, '-vn', '-ac', '1', '-ar', str(sr), '-f', 'wav', tmp_wav]
    try:
        subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=True)
        y, _ = librosa.load(tmp_wav, sr=sr)
        os.remove(tmp_wav)
    except Exception:
        # fallback to librosa directly (less robust)
        try:
            y, _ = librosa.load(video_path, sr=sr)
        except Exception:
            return torch.zeros(frames_len, wav_model.config.hidden_size)
    inp = wav_proc(y, sampling_rate=sr, return_tensors='pt', padding=True).input_values.to(DEVICE)
    if FP16:
        try: inp = inp.half()
        except Exception: pass
    emb = wav_model(inp).last_hidden_state.mean(dim=1).cpu()
    return emb.repeat(frames_len, 1)

# physiology helpers
LEFT_EYE = [36,37,38,39,40,41]
RIGHT_EYE = [42,43,44,45,46,47]

def eye_aspect_ratio(lm, idx):
    p = lm[idx]
    vertical = np.linalg.norm(p[1]-p[5]) + np.linalg.norm(p[2]-p[4])
    horizontal = np.linalg.norm(p[0]-p[3])
    return float(vertical/(2.0*(horizontal+1e-6)))


def extract_rppg_signal(frames):
    means = []
    for f in frames:
        roi = f[f.shape[0]//4:3*f.shape[0]//4, f.shape[1]//4:3*f.shape[1]//4]
        r,g,b = roi[:,:,0].mean(), roi[:,:,1].mean(), roi[:,:,2].mean()
        means.append([r,g,b])
    means = np.array(means)
    means = (means - means.mean(0)) / (means.std(0) + 1e-6)
    X = means[:,0] - means[:,1]
    Y = means[:,0] + means[:,1] - 2*means[:,2]
    return X - Y


def extract_physiology(frames):
    if len(frames) == 0:
        return torch.zeros(0,4)
    rppg = extract_rppg_signal(frames)
    prev = rppg[0]
    physiology = []
    for i, f in enumerate(frames):
        faces = face_app.get(f)
        ear = 0.0; blink = 0.0
        if faces:
            try:
                lm = faces[0].landmark_2d_68
                left = eye_aspect_ratio(lm, LEFT_EYE)
                right = eye_aspect_ratio(lm, RIGHT_EYE)
                ear = (left + right)/2.0
                blink = 1.0 if ear < 0.2 else 0.0
            except Exception:
                ear = 0.0; blink = 0.0
        delta = rppg[i] - prev
        prev = rppg[i]
        physiology.append([rppg[i], float(delta), ear, blink])
    return torch.tensor(physiology, dtype=torch.float32)

