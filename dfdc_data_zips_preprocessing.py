def read_frames(video, max_frames=32):
    cap = cv2.VideoCapture(video)
    frames = []
    while len(frames) < max_frames and cap.isOpened():
        ok, f = cap.read()
        if not ok: break
        frames.append(cv2.cvtColor(f, cv2.COLOR_BGR2RGB))
    cap.release()
    return frames

@torch.no_grad()
def extract_spatial_faces(frames, min_face=40):
    """
    frames: list of HWC uint8 frames
    returns: torch.Tensor [N, C] or None
    """
    embeddings = []

    for frame in frames:
        faces = face_app.get(frame)

        if not faces:
            continue

        face = faces[0]

        # --- SAFE bbox handling ---
        if face.bbox is None:
            continue

        x1, y1, x2, y2 = map(int, face.bbox)

        # skip tiny or invalid boxes
        if x2 <= x1 or y2 <= y1:
            continue
        if (x2 - x1) < min_face or (y2 - y1) < min_face:
            continue

        h, w, _ = frame.shape
        x1, y1 = max(0, x1), max(0, y1)
        x2, y2 = min(w, x2), min(h, y2)

        crop = frame[y1:y2, x1:x2]

        if crop.size == 0:
            continue

        # preprocess for EfficientNet
        crop = cv2.resize(crop, (224, 224))
        crop = torch.from_numpy(crop).permute(2, 0, 1).float() / 255.0
        crop = crop.unsqueeze(0).to(DEVICE)

        feat = spatial_model(crop)  # EfficientNet / Swin
        embeddings.append(feat.squeeze(0).cpu())

    if not embeddings:
        return None

    return torch.stack(embeddings)

def extract_motion(frames):
    feats = []
    prev = None
    for f in frames:
        g = cv2.cvtColor(f, cv2.COLOR_RGB2GRAY)
        if prev is None:
            feats.append(torch.zeros(256))
        else:
            diff = cv2.absdiff(prev, g)
            feats.append(torch.tensor(diff.mean()).repeat(256))
        prev = g
    return torch.stack(feats)

