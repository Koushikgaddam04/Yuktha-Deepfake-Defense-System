@torch.no_grad()
def extract_audio_with_ffmpeg(video_path, sr=AUDIO_SR, frames_count=MAX_FRAMES):
    """Extract audio features for the video using ffmpeg->wav then Wav2Vec2.
       Returns tensor [frames_count, feat_dim] on CPU.
    """
    tmp_wav = os.path.join(TMP_DIR, f"audio_{int(time.time()*1000)}.wav")
    # ffmpeg -y -i input -vn -ac 1 -ar sr -f wav tmp_wav
    cmd = ["ffmpeg", "-y", "-i", video_path, "-vn", "-ac", "1", "-ar", str(sr), "-f", "wav", tmp_wav]
    try:
        subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=True)
    except Exception:
        # fallback: try librosa directly (less robust)
        log("ffmpeg audio extraction failed, falling back to librosa.load on:", video_path)
        try:
            y, sr2 = librosa.load(video_path, sr=sr)
            inp = wav_proc(y, return_tensors="pt", sampling_rate=sr).input_values.to(DEVICE)
            emb = wav_model(inp).last_hidden_state.mean(dim=1)
            emb = emb.cpu()
            return emb.repeat(frames_count, 1)
        except Exception as e:
            log("Audio extraction failed entirely:", e)
            return torch.zeros(frames_count, wav_model.config.hidden_size)
    try:
        y, sr2 = librosa.load(tmp_wav, sr=sr)
        os.remove(tmp_wav)
    except Exception as e:
        log("Failed to load tmp wav:", e)
        if os.path.exists(tmp_wav):
            os.remove(tmp_wav)
        return torch.zeros(frames_count, wav_model.config.hidden_size)
    # process audio to embedding
    inp = wav_proc(y, sampling_rate=sr, return_tensors="pt").input_values.to(DEVICE)
    with torch.no_grad():
        emb = wav_model(inp).last_hidden_state.mean(dim=1).cpu()
    return emb.repeat(frames_count, 1)

