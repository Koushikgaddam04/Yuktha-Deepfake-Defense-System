def copy_zip_to_local(src_zip, dst_zip, attempts=RETRY_COPY_ATTEMPTS):
    # streaming copy with retries
    for attempt in range(1, attempts+1):
        try:
            with open(src_zip, 'rb') as sf, open(dst_zip, 'wb') as df:
                shutil.copyfileobj(sf, df, length=1024*1024)
            if os.path.exists(dst_zip) and os.path.getsize(dst_zip) > 0:
                return True
        except OSError as e:
            log(f'copy attempt {attempt} failed: {e}')
            # remount drive if transport endpoint error
            if getattr(e, 'errno', None) == 107:
                try:
                    drive.mount('/content/drive', force_remount=True)
                    log('Drive remounted after transport error')
                except Exception:
                    pass
            time.sleep(2 ** attempt)
        except Exception as e:
            log('copy exception', e)
            time.sleep(1)
    return False


