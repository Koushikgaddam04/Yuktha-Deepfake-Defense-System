scaler = torch.cuda.amp.GradScaler(enabled=USE_AMP)

def train_epoch_encoder(model, loader, optimizer, epoch, name):
    model.train()
    losses = []
    all_logits = []
    all_labels = []
    tic = time.time()
    for i, batch in enumerate(loader, 1):
        spatial = batch["spatial"].to(DEVICE)
        motion = batch["motion"].to(DEVICE)
        audio = batch["audio"].to(DEVICE)
        phys = batch["phys"].to(DEVICE)
        labels = batch["label"].to(DEVICE)
        optimizer.zero_grad()
        with torch.cuda.amp.autocast(enabled=USE_AMP):
            if name == "face":
                logits, _ = model(spatial)
            elif name == "motion":
                logits, _ = model(motion)
            elif name == "audio":
                logits, _ = model(audio)
            elif name == "phys":
                logits, _ = model(phys)
            else:
                raise ValueError(name)
            loss = F.binary_cross_entropy_with_logits(logits, labels)
        scaler.scale(loss).backward()
        scaler.step(optimizer)
        scaler.update()
        losses.append(loss.item())
        all_logits.append(logits.detach().cpu().numpy())
        all_labels.append(labels.detach().cpu().numpy())
        if i % 50 == 0:
            log(f"{name} Epoch {epoch} step {i}/{len(loader)} loss {np.mean(losses):.4f}")
    toc = time.time()
    logits = np.concatenate(all_logits) if all_logits else np.array([])
    labels = np.concatenate(all_labels) if all_labels else np.array([])
    auc = roc_auc_score(labels, logits) if len(labels)>0 and len(np.unique(labels))>1 else float('nan')
    log(f"{name} Epoch {epoch} finished in {toc-tic:.1f}s loss {np.mean(losses):.4f} train_auc {auc:.4f}")
    return np.mean(losses), auc

@torch.no_grad()
def eval_encoder(model, loader, name):
    model.eval()
    all_logits = []
    all_labels = []
    for batch in loader:
        spatial = batch["spatial"].to(DEVICE)
        motion = batch["motion"].to(DEVICE)
        audio = batch["audio"].to(DEVICE)
        phys = batch["phys"].to(DEVICE)
        labels = batch["label"].to(DEVICE)
        with torch.cuda.amp.autocast(enabled=USE_AMP):
            if name == "face":
                logits, _ = model(spatial)
            elif name == "motion":
                logits, _ = model(motion)
            elif name == "audio":
                logits, _ = model(audio)
            elif name == "phys":
                logits, _ = model(phys)
            else:
                raise ValueError(name)
        all_logits.append(logits.detach().cpu().numpy())
        all_labels.append(labels.detach().cpu().numpy())
    logits = np.concatenate(all_logits) if all_logits else np.array([])
    labels = np.concatenate(all_labels) if all_labels else np.array([])
    auc = roc_auc_score(labels, logits) if len(labels)>0 and len(np.unique(labels))>1 else float('nan')
    log(f"Eval {name}: AUC {auc:.4f}")
    return auc

