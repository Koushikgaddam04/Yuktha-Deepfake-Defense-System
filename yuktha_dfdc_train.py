# Lightweight but powerful encoders (fast on T4)
def make_mlp(in_dim, hidden=512, embed_dim=128, dropout=0.3):
    return nn.Sequential(
        nn.Linear(in_dim, hidden),
        nn.BatchNorm1d(hidden),
        nn.ReLU(inplace=True),
        nn.Dropout(dropout),
        nn.Linear(hidden, embed_dim),
        nn.ReLU(inplace=True)
    )

class FaceEncoder(nn.Module):
    def __init__(self, in_dim, embed_dim=128):
        super().__init__()
        self.enc = make_mlp(in_dim, hidden=512, embed_dim=embed_dim)
        self.classifier = nn.Linear(embed_dim, 1)
    def forward(self, x):
        emb = self.enc(x)
        logit = self.classifier(emb).squeeze(1)
        return logit, emb

class MotionEncoder(nn.Module):
    def __init__(self, in_dim, embed_dim=128):
        super().__init__()
        # in_dim = Dm*2 (mean+std)
        self.enc = make_mlp(in_dim, hidden=512, embed_dim=embed_dim)
        self.classifier = nn.Linear(embed_dim, 1)
    def forward(self, x):
        emb = self.enc(x)
        logit = self.classifier(emb).squeeze(1)
        return logit, emb

class AudioEncoder(nn.Module):
    def __init__(self, in_dim, embed_dim=128, hidden=256):
        super().__init__()
        # We'll use a small BiLSTM over the summarized vector interpreted as sequence of half-length
        # But since we have mean+std summary, we'll treat it as vector and pass through MLP
        self.enc = make_mlp(in_dim, hidden=512, embed_dim=embed_dim)
        self.classifier = nn.Linear(embed_dim, 1)
    def forward(self, x):
        emb = self.enc(x)
        logit = self.classifier(emb).squeeze(1)
        return logit, emb

class PhysEncoder(nn.Module):
    def __init__(self, in_dim, embed_dim=64):
        super().__init__()
        self.enc = make_mlp(in_dim, hidden=256, embed_dim=embed_dim)
        self.classifier = nn.Linear(embed_dim, 1)
    def forward(self, x):
        emb = self.enc(x)
        logit = self.classifier(emb).squeeze(1)
        return logit, emb

class FusionHead(nn.Module):
    def __init__(self, emb_dims: Dict[str,int]):
        super().__init__()
        tot = sum(emb_dims.values())
        self.net = nn.Sequential(
            nn.Linear(tot, 512),
            nn.BatchNorm1d(512),
            nn.ReLU(),
            nn.Dropout(0.3),
            nn.Linear(512, 128),
            nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(128, 1)
        )
    def forward(self, emb_cat):
        return self.net(emb_cat).squeeze(1)

