class DFDCProcessedDataset(Dataset):
    """
    Loads saved feature files for each video.
    FORCE CASTS everything to float32 to prevent Double vs Half errors.
    """
    def __init__(self, root: str, ids: Optional[List[str]] = None):
        self.root = Path(root)
        self.items = []
        for label in ["real", "fake"]:
            p = self.root / label
            if not p.exists():
                continue
            for vid_dir in p.iterdir():
                if not vid_dir.is_dir(): continue
                vid_name = vid_dir.name
                if ids is not None and vid_name not in ids:
                    continue
                meta = vid_dir / "meta.json"
                if not meta.exists(): continue
                with open(meta) as f:
                    m = json.load(f)
                lab = 0 if m.get("label","REAL").upper()=="REAL" else 1
                self.items.append((str(vid_dir), lab))
        self.items = sorted(self.items)
        log("Dataset built with", len(self.items), "videos")

    def __len__(self):
        return len(self.items)

    def _safe_load(self, path: Path):
        try:
            if not path.exists():
                return None
            # Load and immediately ensure it is a float32 tensor
            data = torch.load(str(path))
            if isinstance(data, np.ndarray):
                data = torch.from_numpy(data)
            return data.float() # <--- CRITICAL FIX: Force float32
        except Exception as e:
            return None

    def summarize_temporal(self, x):
        # x: Tensor [T, D] or None
        if x is None:
            return None

        # Ensure x is float32 before processing
        x = x.float()

        if len(x.shape) == 1:
            mean = x
            std = torch.zeros_like(mean)
            return torch.cat([mean, std], dim=0)

        mean = x.mean(dim=0)
        std = x.std(dim=0)
        return torch.cat([mean, std], dim=0)

    def __getitem__(self, idx):
        path, label = self.items[idx]
        p = Path(path)

        # _safe_load now guarantees float32 or None
        spatial = self._safe_load(p / "spatial.pt")
        motion = self._safe_load(p / "motion.pt")
        audio = self._safe_load(p / "audio.pt")
        phys = self._safe_load(p / "physiology.pt")

        # Spatial handling
        if isinstance(spatial, torch.Tensor) and spatial.numel() > 0:
            spatial_mean = spatial.mean(dim=0)
        else:
            spatial_mean = torch.zeros(512).float() # <--- Ensure float32

        motion_sum = self.summarize_temporal(motion)
        if motion_sum is None:
            motion_sum = torch.zeros(256*2).float() # <--- Ensure float32

        audio_sum = self.summarize_temporal(audio)
        if audio_sum is None:
            audio_sum = torch.zeros(512*2).float() # <--- Ensure float32

        phys_sum = self.summarize_temporal(phys)
        if phys_sum is None:
            phys_sum = torch.zeros(4*2).float() # <--- Ensure float32

        return {
            "spatial": spatial_mean,
            "motion": motion_sum,
            "audio": audio_sum,
            "phys": phys_sum,
            "label": torch.tensor(label, dtype=torch.float32),
            "id": Path(path).name
        }

def collate_fn(batch):
    spatial = torch.stack([b["spatial"] for b in batch])
    motion = torch.stack([b["motion"] for b in batch])
    audio = torch.stack([b["audio"] for b in batch])
    phys = torch.stack([b["phys"] for b in batch])
    labels = torch.stack([b["label"] for b in batch])
    ids = [b["id"] for b in batch]
    return {"spatial": spatial, "motion": motion, "audio": audio, "phys": phys, "label": labels, "ids": ids}

