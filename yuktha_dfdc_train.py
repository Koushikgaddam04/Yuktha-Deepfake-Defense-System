# ---------------- fusion dataloader wrapper ----------------
class FusionDatasetWrapper(Dataset):
    def __init__(self, base_dataset):
        self.base = base_dataset
    def __len__(self):
        return len(self.base)
    def __getitem__(self, idx):
        item = self.base[idx]
        return item

fusion_train_loader = DataLoader(FusionDatasetWrapper(train_ds), batch_size=BATCH_SIZE, shuffle=True, num_workers=NUM_WORKERS, collate_fn=collate_fn, pin_memory=True)
fusion_val_loader = DataLoader(FusionDatasetWrapper(val_ds), batch_size=BATCH_SIZE, shuffle=False, num_workers=NUM_WORKERS, collate_fn=collate_fn, pin_memory=True)

# forward helper to get concatenated embeddings
@torch.no_grad()
def get_embeddings_batch(batch):
    spatial = batch["spatial"].to(DEVICE)
    motion = batch["motion"].to(DEVICE)
    audio = batch["audio"].to(DEVICE)
    phys = batch["phys"].to(DEVICE)
    # encoders return (logit, emb)
    _, f_emb = face_enc(spatial)
    _, m_emb = motion_enc(motion)
    _, a_emb = audio_enc(audio)
    _, p_emb = phys_enc(phys)
    return torch.cat([f_emb, m_emb, a_emb, p_emb], dim=1), batch["label"].to(DEVICE)

