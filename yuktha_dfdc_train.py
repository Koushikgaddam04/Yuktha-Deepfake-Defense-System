    fusion.train()
    losses = []
    start = time.time()
    for i, batch in enumerate(fusion_train_loader, 1):
        emb_cat, labels = get_embeddings_batch(batch)
        opt_fusion.zero_grad()
        with torch.cuda.amp.autocast(enabled=USE_AMP):
            logits = fusion(emb_cat)
            loss = F.binary_cross_entropy_with_logits(logits, labels)
        scaler_f.scale(loss).backward()
        scaler_f.step(opt_fusion)
        scaler_f.update()
        losses.append(loss.item())
        if i % 50 == 0:
            log(f"Fusion Epoch {epoch} step {i}/{len(fusion_train_loader)} loss {np.mean(losses):.4f}")
    dur = time.time() - start
    # eval
    fusion.eval()
    all_logits = []
    all_labels = []
    with torch.no_grad():
        for batch in fusion_val_loader:
            emb_cat, labels = get_embeddings_batch(batch)
            out = fusion(emb_cat)
            all_logits.append(out.detach().cpu().numpy())
            all_labels.append(labels.detach().cpu().numpy())
    all_logits = np.concatenate(all_logits) if all_logits else np.array([])
    all_labels = np.concatenate(all_labels) if all_labels else np.array([])
    val_auc = roc_auc_score(all_labels, all_logits) if len(all_labels)>0 and len(np.unique(all_labels))>1 else float('nan')
    log(f"Fusion Epoch {epoch} done in {dur:.1f}s  val_auc {val_auc:.4f}")
