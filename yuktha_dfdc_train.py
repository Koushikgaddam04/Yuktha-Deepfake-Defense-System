# small helper to train & save best
def train_and_save(stream_name, model, epochs=EPOCHS_STREAM):
    optimizer = AdamW(model.parameters(), lr=LR, weight_decay=WEIGHT_DECAY)
    best_auc = 0.0
    best_path = os.path.join(CKPT_DIR, f"{stream_name}_best.pth")
    for ep in range(1, epochs+1):
        train_epoch_encoder(model, train_loader, optimizer, ep, stream_name)
        auc = eval_encoder(model, val_loader, stream_name)
        if not math.isnan(auc) and auc > best_auc:
            best_auc = auc
            torch.save(model.state_dict(), best_path)
            log(f"{stream_name} checkpoint saved to {best_path}")
    stream_results[stream_name] = best_auc
    log(f"{stream_name} best val AUC = {best_auc:.4f}")
    return best_auc

