def copy_file(src_dst):
    src, dst = src_dst
    try:
        if os.path.exists(dst): return # Skip existing
        os.makedirs(os.path.dirname(dst), exist_ok=True)
        shutil.copy2(src, dst)
    except Exception as e:
        print(f"Error copying {src}: {e}")

def fast_copy():
    # 1. Index all files first (Fast)
    print(f"📂 Indexing files in {SOURCE_DIR}...")
    files_to_copy = []

    for root, dirs, files in os.walk(SOURCE_DIR):
        for file in files:
            src_path = os.path.join(root, file)
            # Create corresponding local path
            rel_path = os.path.relpath(src_path, SOURCE_DIR)
            dst_path = os.path.join(DEST_DIR, rel_path)
            files_to_copy.append((src_path, dst_path))

    print(f"✅ Found {len(files_to_copy):,} files. Starting Multi-Threaded Copy...")

    # 2. Parallel Copy (The Speedup)
    start_t = time.time()
    with ThreadPoolExecutor(max_workers=NUM_THREADS) as executor:
        list(tqdm(executor.map(copy_file, files_to_copy), total=len(files_to_copy), unit="file"))

    duration = (time.time() - start_t) / 60
    print(f"🚀 Done! Copied {len(files_to_copy)} files in {duration:.1f} minutes.")

